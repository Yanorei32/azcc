int assert(int, int, char *);
int printf();

int main() {
  assert(1, 1 && 1, "1 && 1");
  assert(0, 1 && 0, "1 && 0");
  assert(0, 0 && 1, "0 && 1");
  assert(0, 0 && 0, "0 && 0");
  assert(1, 1 || 1, "1 || 1");
  assert(1, 1 || 0, "1 || 0");
  assert(1, 0 || 1, "0 || 1");
  assert(0, 0 || 0, "0 || 0");

  assert(0, !(10 == 10), "!(10 == 10)");
  assert(1, !(10 < 10), "!(10 < 10)");
  assert(0, !(20 - 10 * 1 + 2 <= 2 * 7), "!(20 - 10 * 1 + 2 <= 2 * 7)");
  assert(1, !(80 - 50 > 20 * 2), "!(80 - 50 > 20 * 2)");
  assert(0, !(80 - 50 < 20 * 2), "!(80 - 50 < 20 * 2)");
  assert(1, !(80 - 0 <= 20 * 2), "!(80 - 0 <= 20 * 2)");

  assert(0, !(10 == 10) && 10 == 10, "!(10 == 10) && 10 == 10");
  assert(1, !(10 == 10) || 10 == 10, "!(10 == 10) || 10 == 10");
  assert(0, !(1), "!(1)");
  assert(0, !(1) || 10 == 10 && !(10 == 10), "!(1) || 10 == 10 && !(10 == 10)");

  assert(1, 2 && 1, "2 && 1");
  assert(1, 1 && 2 < 174, "1 && 2 < 174");
  assert(1, 2 && 2 < 174, "2 && 2 < 174");
  for (int a = 1; a < 174; a++)
    assert(1, a && a < 174, "a && a < 174");

  assert(174, (3 && 2 && 5) + 173, "(3 && 2 && 5) + 173");

  printf("OK\n");
  return 0;
}
